#include	"ft_printf.h"

void	ft_putstr(char *str, int *len)
{
	if (!str)
	{
		write(1, "(null)", 6);
		*len += 6;
		return ;
	}
	while (*str)
	{
		ft_putchar(*str, len);
		str++;
	}
}
